    private void rButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rButtonActionPerformed
        String rname = rNameField.getText();
        String rpass = rPassField.getText();
        boolean exists = false;
        for(int i = 0;i<userList.size();i++){
            if(userList.get(i).getName().equals(rname)){
                exists = true;
            }
        }

        if(!exists){
            userList.add(new User(rname, rpass, 0, "Seed", 1.0));
            try{
                String filename = "/Users/evanv/OneDrive/Documents/NetBeansProjects/LLProject2/RecyclingProgram/src/users.txt";
                PrintWriter pw = new PrintWriter(filename);
                pw.println(rname+","+rpass+","+0+","+"Tree-Hugger"+","+1);
                pw.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error file not found");
            }
        }
        rNameField.setText("");
        rPassField.setText("");
    }//GEN-LAST:event_rButtonActionPerformed

