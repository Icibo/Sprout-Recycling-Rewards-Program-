    private void redeemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redeemButtonActionPerformed
        String selected = (String)couponBox.getSelectedItem();
        int ind = -1;
        for(int i = 0;i<couponList.size();i++){
            if(selected.equals(couponList.get(i).getTitle())){
                ind = i;
            }
        }
        couponList.get(ind).minusQuant();
        currentUser.minusPoints(couponList.get(ind).getCost());
        pointsLabel.setText("Points: " + currentUser.getPoints());
        couponArea.setText("");
        String rand = "";
        for(int i = 0; i<8;i++){
            rand+= (int)(Math.random()*9)+2;
        }
        JOptionPane.showMessageDialog(null, "Your coupon code is: " + rand);
        for(int i = 0;i<couponList.size();i++){
            Coupon cccoupon = couponList.get(i);
            if(cccoupon.getQuant()>0){
                couponArea.append(cccoupon.getTitle() + ", " + cccoupon.getCost() + " Points, " + cccoupon.getQuant() + " Remaining" + "\n");
                couponBox.addItem(cccoupon.getTitle());
            }
        }
        rewriteUsers();
        rewriteCoupons();
        
    }//GEN-LAST:event_redeemButtonActionPerformed

