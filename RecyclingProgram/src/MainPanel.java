    private void recycleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recycleButtonActionPerformed
        int grams = Integer.parseInt(weightField.getText());
        double multi = currentUser.getTierMulti();
        int result = (int)(grams*multi);
        currentUser.addPoints(result);
        weightField.setText("");
        JOptionPane.showMessageDialog(null, result + " points added");
        if(currentUser.getPoints()>=1000){
            currentUser.setTier("Sprout");
            currentUser.setMTier(2);
        }
        if(currentUser.getPoints()>=10000){
            currentUser.setTier("Plant");
            currentUser.setMTier(3);
        }
        pointsLabel.setText("Points: " + currentUser.getPoints());
        tierLabel.setText( "Tier: " + currentUser.getTier() + " (" + currentUser.getTierMulti() + "x)");
        rewriteUsers();
        
    }//GEN-LAST:event_recycleButtonActionPerformed

