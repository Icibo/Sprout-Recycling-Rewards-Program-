    private void logInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInButtonActionPerformed
        String uname = nameField.getText();
        String upass = passField.getText();
        int ind = -1;
        for(int i = 0; i<userList.size();i++){
            if(uname.equals(userList.get(i).getName())){
                ind = i;
            }
        }
        if(userList.get(ind).getPass().equals(upass)){
            currentUser = userList.get(ind);
            nameLabel.setText("Username: " + uname);
            tierLabel.setText("Tier: " + currentUser.getTier() + " (" + currentUser.getTierMulti() +"x)");
            pointsLabel.setText("Points: " + currentUser.getPoints());
        }
        else if(!userList.get(ind).getPass().equals(upass) || ind == -1){
            JOptionPane.showMessageDialog(null, "Username or Password is incorrect");
        }
        nameField.setText("");
        passField.setText("");
    }//GEN-LAST:event_logInButtonActionPerformed

